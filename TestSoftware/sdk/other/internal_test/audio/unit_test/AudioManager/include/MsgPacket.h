	template<typename T>
	void setParam(const T& param, bool type_check) {
		if (type_check) {
			m_flags |= MsgFlagTypedParam;
			*((T *)&m_param[0]) = param;
		} else {
			*((T *)&m_param[0]) = param;
		}
		m_param_size = sizeof(T);
		MEMORY_BARRIER();
		m_flags &= ~MsgFlagWaitParam;	/* パラメタ書込み待ちフラグをクリア */
	}
	
	
	
	
