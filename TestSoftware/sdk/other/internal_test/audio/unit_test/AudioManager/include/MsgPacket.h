typedef uint16_t MsgType;	/* メッセージタイプID */
typedef uint16_t MsgQueId;	/* メッセージキューID */
typedef uint8_t  MsgCpuId;	/* メッセージCPU-ID */
typedef uint8_t  MsgFlags;	/* メッセージフラグ */

enum MsgPri {			/* メッセージ優先度 */
	MsgPriNormal,
	MsgPriHigh,
	NumMsgPri		/* 優先度の数 */
};

/*****************************************************************
 * メッセージパケットヘッダクラス
 *****************************************************************/
class MsgPacketHeader {
public:
	/* 基本的に各フラグは、MessageLib内部専用とする予定 */
	static const MsgFlags MsgFlagNull	= 0x00;	/* フラグは空 */
	static const MsgFlags MsgFlagWaitParam	= 0x80;	/* パラメタ書込み待ち */
	static const MsgFlags MsgFlagTypedParam = 0x40; /* パラメータは型を持った形式 */
	MsgPacketHeader(MsgType type, MsgQueId reply, MsgFlags flags, uint16_t size = 0) :
		m_type(type),
		m_reply(reply),
		m_src_cpu(GET_CPU_ID()),
		m_flags(flags),
		m_param_size(size) {}

	MsgType  getType() const { return m_type; }
	MsgQueId getReply() const { return m_reply; }
	MsgCpuId getSrcCpu() const { return m_src_cpu; }
	MsgFlags getFlags() const { return m_flags; }
	uint16_t getParamSize() const { return m_param_size; }
	void     popParamNoDestruct() { m_param_size = 0; }

//protected:
	bool isSelfCpu() const { return GET_CPU_ID() == getSrcCpu(); }
	bool isTypedParam() const { return (m_flags & MsgFlagTypedParam) != 0; }

//protected:
	MsgType		m_type;
	MsgQueId	m_reply;
	MsgCpuId	m_src_cpu;
	MsgFlags	m_flags;
	uint16_t	m_param_size;
}; /* class MsgPacketHeader */

/*****************************************************************
 * メッセージパラメタが存在しないことを示すクラス
 *****************************************************************/
class MsgNullParam {};

/*****************************************************************
 * アドレス範囲パラメタであることを示すクラス
 *****************************************************************/
class MsgRangedParam {
public:
	MsgRangedParam(const void* param, size_t param_size) :
		m_param(param),
		m_param_size(param_size) {}
	const void*	getParam() const { return m_param; }
	size_t		getParamSize() const { return m_param_size; }

private:
	const void*	m_param;
	size_t		m_param_size;
};

